public class Pattern1 {
    public static void main(String[] args){
        int n = 5;
    
        for(int i=1;i<=9;i++){
                for(int j=1;j<=4;j++){

                    if(i==(n/2))
                    {
                        System.out.print("*");
                    }
                    else{
                        if(j==(n/2))
                        {
                            System.out.print("*");

                        }
                        else{
                            System.out.print(" ");

                        }
                    }}
                    System.out.println(" ");
                    }


                    }
}

